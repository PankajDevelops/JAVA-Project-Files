public class Variables {
    public static void main(String args[]) {

        int a = 10;
        int b = 30;
        int c = 12;
        System.out.println(a);

        java.lang.String str = "Allen";
        System.out.println(str);

        a = 20;
        b = c;
        System.out.println(a);
        System.out.println(b);
        
    }

}